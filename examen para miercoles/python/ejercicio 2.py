#2️⃣ Verificador de Edad para Votar 🗳
#   - Enunciado: Un programa que te dice si tienes edad para votar.
#    - Requerimientos: Pedir la edad, usar un if-else y mostrar el mensaje correspondien

edad = int(input("ponga su edad:"))

if edad >= 18:
    print("usted tiene la edad suificiciente para votar")

else:
    print("no tienes la edad necesaria para votar")
